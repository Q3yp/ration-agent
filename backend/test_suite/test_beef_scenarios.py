async def login(client: httpx.AsyncClient) -> str:
    """Login and return JWT token"""
    # FastAPI-Users JWT login expects form data
    login_data = {
        "username": TEST_EMAIL,
        "password": TEST_PASSWORD
    }

    response = await client.post(
        f"{API_BASE_URL}/auth/jwt/login",
        data=login_data  # Use data= for form data, not json=
    )

    if response.status_code != 200:
        raise Exception(f"Login failed: {response.status_code} - {response.text}")

    data = response.json()
    return data["access_token"]


async def create_session(client: httpx.AsyncClient, token: str) -> str:
    """Create a new session for beef cattle and return session_id"""
    headers = {"Authorization": f"Bearer {token}"}

    response = await client.post(
        f"{API_BASE_URL}/sessions/create",
        json={"animal_type": "beef_cow"},
        headers=headers
    )

    if response.status_code != 200:
        raise Exception(f"Session creation failed: {response.status_code} - {response.text}")

    data = response.json()
    return data["session_id"]


async def send_message(client: httpx.AsyncClient, token: str, session_id: str, message: str):
    """Send a message to the chat endpoint (fire and forget)"""
    headers = {"Authorization": f"Bearer {token}"}

    response = await client.post(
        f"{API_BASE_URL}/chat/stream/{session_id}",
        json={"message": message},
        headers=headers,
        timeout=5.0  # Short timeout since we're not waiting for completion
    )

    # We don't care about the response, just that it was accepted
    if response.status_code not in [200, 202]:
        print(f"  ⚠️  Warning: Message send returned {response.status_code}")


async def run_scenario(client: httpx.AsyncClient, token: str, scenario: Dict):
    """Create session and send prompt for a single scenario"""
    print(f"\n{'='*80}")
    print(f"场景 {scenario['id']}: {scenario['name']}")
    print(f"{'='*80}")

    try:
        # Create session
        print("  创建会话中...")
        session_id = await create_session(client, token)
        print(f"  ✓ 会话已创建: {session_id}")

        # Send prompt
        print("  发送配方请求...")
        await send_message(client, token, session_id, scenario['prompt'])
        print(f"  ✓ 请求已发送（代理将自主完成配方）")

        return {
            "scenario_id": scenario['id'],
            "scenario_name": scenario['name'],
            "session_id": session_id,
            "status": "submitted"
        }

    except Exception as e:
        print(f"  ✗ 错误: {str(e)}")
        return {
            "scenario_id": scenario['id'],
            "scenario_name": scenario['name'],
            "session_id": None,
            "status": "failed",
            "error": str(e)
        }


async def main():
    """Run all beef cattle scenarios concurrently"""
    print("="*80)
    print("肉牛配方场景自动化测试")
    print("="*80)
    print(f"测试账号: {TEST_EMAIL}")
    print(f"场景总数: {len(SCENARIOS)}")
    print(f"API地址: {API_BASE_URL}")
    print("="*80)

    async with httpx.AsyncClient(timeout=10.0) as client:
        # Login
        print("\n登录中...")
        try:
            token = await login(client)
            print("✓ 登录成功")
        except Exception as e:
            print(f"✗ 登录失败: {e}")
            return

        # Fire all scenarios concurrently
        print(f"\n🚀 并发提交所有 {len(SCENARIOS)} 个场景...")
        tasks = [run_scenario(client, token, scenario) for scenario in SCENARIOS]
        results = await asyncio.gather(*tasks, return_exceptions=True)

        # Process results
        successful = []
        failed = []

        for i, result in enumerate(results):
            if isinstance(result, Exception):
                failed.append({
                    "scenario_id": SCENARIOS[i]['id'],
                    "scenario_name": SCENARIOS[i]['name'],
                    "error": str(result)
                })
            elif result['status'] == 'submitted':
                successful.append(result)
            else:
                failed.append(result)

        # Print summary
        print(f"\n{'='*80}")
        print("测试完成汇总")
        print(f"{'='*80}")

        print(f"\n成功提交: {len(successful)}/{len(SCENARIOS)}")
        print(f"失败: {len(failed)}/{len(SCENARIOS)}")

        if successful:
            print(f"\n✅ 已提交的会话 ID:")
            for r in successful:
                print(f"  场景{r['scenario_id']:2d} ({r['scenario_name']}): {r['session_id']}")

        if failed:
            print(f"\n❌ 失败的场景:")
            for r in failed:
                print(f"  场景{r['scenario_id']:2d} ({r['scenario_name']}): {r.get('error', 'Unknown error')}")

        print(f"\n{'='*80}")
        print("所有场景已提交，代理正在后台自主完成配方")
        print("请在前端查看会话进度和导出结果")
        print(f"{'='*80}\n")


if __name__ == "__main__":
    asyncio.run(main())
